const mongoose = require('mongoose');

const factSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Fact = mongoose.model('Fact', factSchema);

module.exports = Fact;
